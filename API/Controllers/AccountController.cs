namespace API.Controllers;

public class AccountController(AppDbContext context, ITokenService tokenService) : BaseApiController
{
    [HttpPost("register")] //api/account/register
    public async Task<ActionResult<UserDto>> Register(RegisterDto registerDto)
    {
        if (await EmailExists(registerDto.Email)) return BadRequest("Email already exist");
        using var hmac = new HMACSHA512(); // helps to hash  password  
        var user = new AppUser
        {
            DisplayName = registerDto.DisplayName,
            Email = registerDto.Email,
            PasswordHash = hmac.ComputeHash(Encoding.UTF8.GetBytes(registerDto.Password)),
            PasswordSalt = hmac.Key
        };

        context.Users.Add(user);
        //        return new UserDto
        // {
        //     Id = user.Id,
        //     DisplayName = user.DisplayName,
        //     Email = user.Email,
        //     Token = tokenService.CreateToken(user)
        // };
             await context.SaveChangesAsync();
         return user.ToDto(tokenService);
    }


    [HttpPost("login")]

    public async Task<ActionResult<UserDto>> Login(loginDto loginDto)  // Action result to specify the type of thing to return 
    {
        var user = await context.Users.SingleOrDefaultAsync(x => x.Email == loginDto.Email);
        if (user == null)
        {
            return Unauthorized("Invalid email address");
        }

        using var hmac = new HMACSHA512(user.PasswordSalt);
        var computedHash = hmac.ComputeHash(Encoding.UTF8.GetBytes(loginDto.Password));
        for (var i = 0; i < computedHash.Length; i++)
        {
            if (computedHash[i] != user.PasswordHash[i])
                return Unauthorized("Invalid Password");

        }
             //        return new UserDto
        // {
        //     Id = user.Id,
        //     DisplayName = user.DisplayName,
        //     Email = user.Email,
        //     Token = tokenService.CreateToken(user)
        // };y
        return user.ToDto(tokenService);
    

    }
    private async Task<Boolean> EmailExists(string email) // to check if the email exist before 
    {
        return await context.Users.AnyAsync(x => x.Email.ToLower() == email.ToLower());
    }

}

